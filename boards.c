void getBoard(Board b, int counter[], int *longest) 
{
    for(int i = 0; i < 10; ++i) {
          for (int j = 0; j < 10; ++j)
               b[i][j] = p[0][j][i];
    }
    counter[1] = 3;
    counter[2] = 3;
    counter[3] = 2;
    counter[4] = 1;
    counter[5] = 1; 
    *longest = 5;
}

void randBoard(Board b, int counter[], int *longest, char n) 
{
    //sleep(1);
    srand(time(0) * n);
    int ind = rand() % 9;
    for (int i = 0; i < 10; ++i) {
        for (int j = 0; j < 10; ++j) {
            b[i][j] = p[ind][j][i];
        }
    }
    counter[1] = 3;
    counter[2] = 3;
    counter[3] = 2;
    counter[4] = 1;
    counter[5] = 1;
    *longest = 5;
}
